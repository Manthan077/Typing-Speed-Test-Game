export const getParagraphsByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): Paragraph[] => {
  return paragraphs.filter(p => p.difficulty === difficulty);
};

export const getRandomParagraph = (difficulty: 'easy' | 'medium' | 'hard'): Paragraph => {
  let randomTexts: string[];
  
  switch (difficulty) {
    case 'easy':
      randomTexts = easyRandomTexts;
      break;
    case 'medium':
      randomTexts = mediumRandomTexts;
      break;
    case 'hard':
      randomTexts = hardRandomTexts;
      break;
    default:
      randomTexts = easyRandomTexts;
  }
  
  const randomText = randomTexts[Math.floor(Math.random() * randomTexts.length)];
  
  return {
    id: `random-${difficulty}-${Date.now()}`,
    difficulty,
    title: `Random ${difficulty.charAt(0).toUpperCase() + difficulty.slice(1)} Challenge`,
    text: randomText
  };
};